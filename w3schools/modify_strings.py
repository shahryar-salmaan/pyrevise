# Modify Strings

a: str = "Sky is blue."

print(a.upper()) # Uppercase
print(a.lower()) # Lowercase

b: str = "  Green looks good on you!  "
print(b.strip()) # Remove whitespaces

c: str = "Yellow, Cyan, Pink"
print(c.replace("Pink", "Blue")) # Replace

d: str = "Tic-Tac-Toe"
print(d.split("-")) # Split
